package com.example.intefacerasteroid;

public class User {
    private Integer IpNumber;
    private String UserName;
    private Integer SpaceShipType;

    public Integer getIpNumber() {
        return IpNumber;
    }

    public void setIpNumber(Integer ipNumber) {
        this.IpNumber = ipNumber;
    }

    public String getUserName() {
        return UserName;
    }

    public void setUserName(String userName) {
        this.UserName = userName;
    }

    public Integer getSpaceShipType() {
        return SpaceShipType;
    }

    public void setSpaceShipType(Integer spaceShipType) {
        this.SpaceShipType = spaceShipType;
    }
}
